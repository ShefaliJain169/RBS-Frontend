export class Employee {
        id: number;
        firstName: string;
        lastName: string;
        email: string;
        salary: number;

        constructor(id: number, firstName: string, lastName: string, email: string, salary: number) {
            this.id = id;
            this.firstName = firstName;
            this.lastName = firstName;
            this.email = email;
            this.salary = salary;
    }
}
